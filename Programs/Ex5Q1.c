#include <stdio.h>

int main() {
    printf("%d\n", 22 / 7);  // Integer division
    printf("%d\n", 7 / 22);  // Integer division
    printf("%d\n", 7 % 22);  // Modulus operation
    printf("%d\n", 22 % 7);  // Modulus operation

    printf("%d\n", 15 / 16);  // Integer division
    printf("%d\n", 16 / 15);  // Integer division
    printf("%d\n", 15 % 16);  // Modulus operation
    printf("%d\n", 16 % 15);  // Modulus operation
    
    printf("%d\n", 23 / 3);  // Integer division
    printf("%d\n", 3 / 23);  // Integer division
    printf("%d\n", 3 % 23);  // Modulus operation
    printf("%d\n", 23 % 3);  // Modulus operation
    
    printf("%d\n", -3 / 16);  // Integer division
    printf("%d\n", 16 / -3);  // Integer division
    printf("%d\n", -3 % 16);  // Modulus operation
    printf("%d\n", 16 % -3);  // Modulus operation



    return 0;
}